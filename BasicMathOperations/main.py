# Basic math operations
# Module import example

import BasicMath as bmath

a = 10
b = 5

s = bmath.sum_(a, b)
sub = bmath.subtract(a, b)
m = bmath.multiply(a, b)
d = bmath.divide(a, b)

print(f'Basic Operation Resulto for a = {a} and b = {b} are: '
      f'sum = {s}, subtract = {sub}, multiply = {m} and division = {d}')
